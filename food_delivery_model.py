#@title Code - Define functions to make predictions
def format_time(minutes):
    seconds = int(minutes * 60)
    h = seconds // 3600
    m = (seconds % 3600) // 60
    s = seconds % 60
    return f"{h:02d}:{m:02d}:{s:02d}"

def build_batch(df, batch_size):
    batch = df.sample(n=batch_size).copy()
    batch.set_index(np.arange(batch_size), inplace=True)
    return batch

def predict_delivery_time(model, df, features, label, batch_size=2):
  batch = build_batch(df, batch_size)
  predicted_values = model.predict_on_batch(x={name: batch[name].values for name in features})

  data = {"PREDICTED_DELIVERY_TIME": [], "OBSERVED_DELIVERY_TIME": [], "L1_LOSS": [],
          features[0]: [], features[1]: [], features[2]: []}
  for i in range(batch_size):
      predicted = predicted_values[i][0]
      observed = batch.at[i, label]
      data["PREDICTED_DELIVERY_TIME"].append(format_time(predicted))
      data["OBSERVED_DELIVERY_TIME"].append(format_time(observed))
      data["L1_LOSS"].append(format_time(abs(observed - predicted)))
      data[features[0]].append(batch.at[i, features[0]])
      data[features[1]].append(format(batch.at[i, features[1]]))
      data[features[2]].append(format(batch.at[i, features[2]]))
  output_df = pd.DataFrame(data)
  return output_df

def show_predictions(output):
  header = "-" * 80
  banner = header + "\n" + "|" + "PREDICTIONS".center(78) + "|" + "\n" + header
  print(banner)
  print(output)
  return

